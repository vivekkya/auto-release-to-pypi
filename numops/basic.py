class basic:

    def sum(self, a, b):
        """
        Sum of two numbers

        Args:
            a (int): Number 1
            b (int): Number 2

        Returns:
            int: Sum of both the numbers
        """

        return a+b

    def difference(self, a, b):
        """
        Difference of two numbers

        Args:
            a (int): Number 1
            b (int): Number 2

        Returns:
            int: Difference of both the numbers
        """
        
        return a-b

    def multiplication(self, a,b):
        """
        Multiplication of two numbers

        Args:
            a (int): Number 1
            b (int): Number 2

        Returns:
            int: Multiplication of both the numbers
        """        

        return a*b


